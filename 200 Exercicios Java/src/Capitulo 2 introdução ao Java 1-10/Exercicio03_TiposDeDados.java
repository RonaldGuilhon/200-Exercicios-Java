public class Exercicio03_TiposDeDados {
    public static void main(String[] args) {

        // declarção das variaveis (Tipos primitivos)
        int idade = 30;
        double altura = 1.75;
        float salario = 2500.50f;
        boolean trabalhadorAtivo = true;
        byte numeroDeFilhos = 3;
        long distanciaResidencia = 384400000L;
        short anosDeEmpresa = 15;
        char inicialDoNome = 'R';

        System.out.println("idade: " + idade + " anos");
        System.out.println("Altura: " + altura + " m");
        System.out.println("salário: " + salario + " R$");
        System.out.println("Trabalhador ativo: " + trabalhadorAtivo);
        System.out.println("Número de filhos: " + numeroDeFilhos);
        System.out.println("Distância da residencia: " + distanciaResidencia + " Km");
        System.out.println("Anos de empresa: " + anosDeEmpresa + " ano(s)");
        System.out.println("Inicial do nome: " + inicialDoNome);
    }
}
