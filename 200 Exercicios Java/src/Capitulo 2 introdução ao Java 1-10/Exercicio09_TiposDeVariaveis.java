public class Exercicio09_TiposDeVariaveis {
    // variavel global
    static int variavelGlobal = 10;

    public static void main(String[] args) {
        // variavel local
        int variavelLocal = 5;

        System.out.println("A variavel global é: " + variavelGlobal);
        System.out.println("A variavel local é: " + variavelLocal);

    }
}