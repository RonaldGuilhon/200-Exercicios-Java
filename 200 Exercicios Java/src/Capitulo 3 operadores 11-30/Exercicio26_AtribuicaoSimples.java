public class Exercicio26_AtribuicaoSimples {
    public static void main(String[] args) {

        int variavel = 10;

        variavel += 5; // variavel + 5
        System.out.println("Após atribuição +=: " + variavel);

        variavel -= 5; // variavel - 5
        System.out.println("Após atribuição -=: " + variavel);

        variavel *= 5; // variavel * 5
        System.out.println("Após atribuição *=: " + variavel);

        variavel /= 5; // variavel / 5
        System.out.println("Após atribuição /=: " + variavel);

        variavel %= 5; // variavel modulo de 5
        System.out.println("Após atribuição %=: " + variavel);

    }
}
