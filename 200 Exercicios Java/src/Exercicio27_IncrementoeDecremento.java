public class Exercicio27_IncrementoeDecremento {
    public static void main(String[] args) {
        int x = 10;
       
        System.out.println("Valor inicial da variável x: " + x);

        x++;
        System.out.println("Valor da variável x após o incremento (x++): " + x);

        x--;
        System.out.println("Valor da variável x após o decremento (x--): " +x);


    }

}
